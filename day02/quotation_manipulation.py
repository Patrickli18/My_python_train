#Quotation Manipulation
# Character printing methods in Python

# IBM Thomas Watson 
quote = "I think there is a world market for maybe five computers."
print("Original quote:" )
print(quote)

print("\nIn uppercase:")
print(quote.upper())

print("\nIn lowercase:")
print(quote.lower())

print("\nAs a title:" )
print(quote.title())

print("\nWith amiinor replacement:" )
print(quote.replace("computers", "typewriters"))

print("\nOriginal quote is still:")
print(quote)

